# 4. ДЕСКРИПТОРЫ
# =============================================================================
"""
Дескриптор — это класс, который управляет доступом к атрибуту другого класса.

Протокол дескриптора (достаточно реализовать хотя бы один метод):
  __get__(self, obj, owner)   — вызывается при ЧТЕНИИ атрибута (obj.attr)
  __set__(self, obj, value)   — вызывается при ЗАПИСИ (obj.attr = value)
  __delete__(self, obj)       — вызывается при УДАЛЕНИИ (del obj.attr)

self  — сам дескриптор
obj   — объект, к атрибуту которого обратились
owner — класс этого объекта
value — присваиваемое значение

Дескриптор создаётся ОДИН раз на уровне класса:
  class User:
      name = ValidatedString()  # дескриптор — атрибут класса

При user.name = "Анна" вызывается ValidatedString.__set__(descriptor, user, "Анна")
При user.name вызывается ValidatedString.__get__(descriptor, user, User)

На дескрипторах построены:
  - @property       (дескриптор с __get__ и опционально __set__)
  - @staticmethod   (дескриптор, возвращающий функцию без self)
  - @classmethod    (дескриптор, подставляющий cls вместо self)

Дескрипторы vs @property:
  - @property — для одного атрибута в одном классе
  - Дескриптор — переиспользуемый компонент для десятков классов
"""

class PositiveNumber:
    """Только положительные числа (int или float)."""
    def __get__(self, obj, owner):
        return obj._value
    
    def __set__(self, obj, value):
        if not isinstance(value, (int, float)):
            raise TypeError(f"Должно быть число, не {type(value).__name__}")
        if value < 0:
            raise ValueError(f"Не может быть отрицательным! Получено: {value}")
        obj._value = value  # сохраняем в ОБЪЕКТЕ, не в дескрипторе!


class ValidatedString:
    """Строка с проверкой минимальной и максимальной длины."""
    def __init__(self, min_length=1, max_length=100):
        self.min_length = min_length
        self.max_length = max_length
    
    def __get__(self, obj, owner):
        return obj._value
    
    def __set__(self, obj, value):
        if not isinstance(value, str):
            raise TypeError(f"Должна быть строка, не {type(value).__name__}")
        if len(value) < self.min_length:
            raise ValueError(f"Длина должна быть >= {self.min_length}. Получено: {len(value)}")
        if len(value) > self.max_length:
            raise ValueError(f"Длина должна быть <= {self.max_length}. Получено: {len(value)}")
        obj._value = value


class RangedNumber:
    """Число в заданном диапазоне [min_value, max_value]."""
    def __init__(self, min_value=0, max_value=100):
        self.min_value = min_value
        self.max_value = max_value
    
    def __get__(self, obj, owner):
        return obj._value
    
    def __set__(self, obj, value):
        if not isinstance(value, (int, float)):
            raise TypeError(f"Должно быть число, не {type(value).__name__}")
        if not (self.min_value <= value <= self.max_value):
            raise ValueError(f"Значение должно быть от {self.min_value} до {self.max_value}. Получено: {value}")
        obj._value = value


class NotEmptyString:
    """Непустая строка (аналог ValidatedString(min_length=1), но только проверка на пустоту)."""
    def __get__(self, obj, owner):
        return obj._value
    
    def __set__(self, obj, value):
        if not isinstance(value, str):
            raise TypeError("Должна быть строка!")
        if value == "":
            raise ValueError("Строка не может быть пустой!")
        obj._value = value

