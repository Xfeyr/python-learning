# 14. СТЕК И ОЧЕРЕДЬ С МИНИМУМОМ ЗА O(1)
# =============================================================================
"""
Задача: структура данных, поддерживающая push, pop и get_min за O(1).

MinStack (стек с минимумом):
  Храним пары (значение, текущий_минимум_в_стеке).
  При push: минимум = min(новое_значение, предыдущий_минимум)
  get_min: просто возвращаем второй элемент верхней пары → O(1)

MinQueue (очередь с минимумом):
  Очередь = два стека: stack_in (для push) и stack_out (для pop).
  
  push(value): просто кладём в stack_in
  
  pop(): если stack_out пуст — ПЕРЕКЛАДЫВАЕМ всё из stack_in в stack_out
         (элементы переворачиваются, первый зашёл → первый вышел)
         затем pop из stack_out
  
  get_min(): минимум из двух get_min() стеков:
         min(stack_in.get_min(), stack_out.get_min())

  Перекладывание — O(n), но происходит РЕДКО (амортизированная стоимость O(1)).

  В реальном коде используют collections.deque, но эта задача —
  классика спортивного программирования (Codeforces, LeetCode).
"""

class MinStack:
    def __init__(self):
        self.stack = []
    
    def push(self, value):
        if not self.stack:
            self.stack.append((value, value))
        else:
            self.stack.append((value, min(value, self.stack[-1][1])))
    
    def pop(self):
        if self.stack:
            return self.stack.pop()[0]
        return None
    
    def get_min(self):
        if self.stack:
            return self.stack[-1][1]
        return None
    
    def is_empty(self):
        return len(self.stack) == 0


class MinQueue:
    def __init__(self):
        self.stack_in = MinStack()
        self.stack_out = MinStack()
    
    def push(self, value):
        self.stack_in.push(value)
    
    def pop(self):
        if self.stack_out.is_empty():
            while not self.stack_in.is_empty():
                self.stack_out.push(self.stack_in.pop())
        return self.stack_out.pop()
    
    def get_min(self):
        a = self.stack_in.get_min()
        b = self.stack_out.get_min()
        if a is None:
            return b
        if b is None:
            return a
        return min(a, b)

