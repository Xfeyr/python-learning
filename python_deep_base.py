# 3. МЕТАКЛАССЫ
# =============================================================================
"""
Метакласс — это класс, который создаёт ДРУГИЕ классы.
Если type — станок по умолчанию, то метакласс — тюнингованный станок.

Как работает:
  1. Наследуемся от type
  2. Переопределяем __new__(cls, name, bases, dct)
  3. В __new__ можно ИЗМЕНИТЬ dct (словарь методов) до создания класса
  4. Возвращаем super().__new__(cls, name, bases, modified_dct)

__new__ вызывается ОДИН раз — при создании класса.
Не путать с __init__, который вызывается при создании ЭКЗЕМПЛЯРА.

Параметры __new__:
  cls   — сам метакласс (например, LoggedMeta)
  name  — имя создаваемого класса (строка)
  bases — кортеж родителей
  dct   — словарь с методами и атрибутами будущего класса

LoggedMeta оборачивает ВСЕ пользовательские методы в логгер.
Магические методы (__init__, __str__) не трогаем — проверка key.startswith('__').
"""

from types import FunctionType

class LoggedMeta(type):
    def __new__(cls, name, bases, dct):
        new_dct = {}
        for key, value in dct.items():
            # Функция И не магический метод
            if isinstance(value, FunctionType) and not key.startswith('__'):
                new_dct[key] = cls._wrap_method(key, value)
            else:
                new_dct[key] = value
        return super().__new__(cls, name, bases, new_dct)
    
    @staticmethod
    def _wrap_method(name, original):
        """Создаёт обёртку, которая логирует вызов метода."""
        def wrapper(self, *args, **kwargs):
            print(f"Вызов метода {name} с аргументами {args}")
            result = original(self, *args, **kwargs)
            print(f"Метод {name} завершён")
            return result
        return wrapper

# Использование:
# class MyClass(metaclass=LoggedMeta):
#     def say_hello(self, name):
#         return f"Привет, {name}!"
# obj = MyClass()
# obj.say_hello("Мир")  # Вызов метода say_hello с аргументами ('Мир',)
#                       # Метод say_hello завершён
#                       # 'Привет, Мир!'

