# 9. __slots__ — ЭКОНОМИЯ ПАМЯТИ
# =============================================================================
"""
Обычно атрибуты объекта хранятся в словаре __dict__.
Это удобно (можно добавлять любые атрибуты на лету), но словарь жрёт память:
  - Сам словарь: ~200 байт
  - Плюс хеш-таблица для ключей

__slots__ — строгий перечень разрешённых атрибутов.
При использовании __slots__ интерпретатор НЕ создаёт __dict__.
Атрибуты хранятся в фиксированных слотах (как в C-структуре).

Результат (замер на 100 000 объектов через pympler):
  Без __slots__: 264 байт/объект
  Со __slots__:   80 байт/объект
  Экономия: в 3.3 раза (184 байта на каждом объекте)

Ограничения:
  - Нельзя добавить новый атрибут (AttributeError)
  - Нельзя использовать с некоторыми продвинутыми фичами (слабые ссылки без __weakref__)
  - Наследование: дочерний класс должен явно указать свои __slots__

Когда использовать:
  - Миллионы объектов (игровые частицы, строки данных)
  - Классы с фиксированным набором полей (User, Product, Vector3)
"""

class WithSlots:
    __slots__ = ('x', 'y')
    def __init__(self, x, y):
        self.x = x
        self.y = y

