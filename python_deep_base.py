"""
Индекс — структура данных, ускоряющая поиск по колонке.
Как алфавитный указатель в книге: вместо перебора ВСЕХ страниц,
смотрим в указатель и сразу переходим к нужной.

Без индекса: SQL перебирает ВСЕ строки (table scan / sequential scan)
С индексом:  SQL идёт по B-дереву и находит за O(log n)

Создание:
  CREATE INDEX имя_индекса ON таблица(колонка)

Результаты на 100 000 строк:
  Без индекса: 0.01 сек
  С индексом:  0.0006 сек
  Ускорение:   в 16 раз

Когда создавать:
  ✅ Колонки, по которым часто ищут (WHERE)
  ✅ Колонки для сортировки (ORDER BY)
  ✅ Внешние ключи для JOIN (user_id, game_id)
  ❌ Каждую колонку подряд (индексы замедляют INSERT и UPDATE)
  ❌ Маленькие таблицы (< 1000 строк)

Типы индексов в PostgreSQL:
  B-Tree — для сравнений (=, >, <)
  GIN    — для полнотекстового поиска
  GiST   — для геометрических данных
  BRIN   — для очень больших отсортированных таблиц
"""

