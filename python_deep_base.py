"""
База данных — это структурированное хранилище данных на диске.
В отличие от переменных в Python, данные НЕ исчезают после закрытия программы.

SQLite:
  - Встроена в Python (модуль sqlite3)
  - Вся БД — один файл .db
  - Не требует установки сервера
  - Подходит для настольных приложений, игр, прототипов

Основные понятия:
  Таблица   — как лист в Excel (строки и столбцы)
  Строка    — одна запись (один пользователь, один товар)
  Столбец   — поле с данными одного типа (имя, возраст, цена)
  Первичный ключ (PRIMARY KEY) — уникальный идентификатор строки

Основные команды SQL (CRUD):
  CREATE TABLE имя (колонка ТИП, ...)     — создать таблицу
  INSERT INTO имя VALUES (?, ?)           — добавить строку
  SELECT * FROM имя                        — прочитать всё
  SELECT * FROM имя WHERE условие          — с фильтром
  SELECT * FROM имя ORDER BY колонка DESC  — с сортировкой
  UPDATE имя SET колонка = ? WHERE ...     — обновить
  DELETE FROM имя WHERE ...                — удалить

Агрегатные функции:
  COUNT(*)  — количество строк
  SUM(колонка) — сумма значений
  AVG(колонка) — среднее
  MAX, MIN    — максимум, минимум

Типы данных SQLite: TEXT, INTEGER, REAL, BLOB

Порядок работы в Python:
  1. conn = sqlite3.connect("файл.db")   — подключиться
  2. cursor = conn.cursor()              — создать курсор
  3. cursor.execute("SQL", (параметры,)) — выполнить запрос
  4. conn.commit()                        — сохранить изменения
  5. conn.close()                         — закрыть

Знаки ? в запросе — заглушки для безопасной подстановки значений.
НИКОГДА не вставляй значения через f-строки в SQL — это уязвимость (SQL-инъекция).
"""

