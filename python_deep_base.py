"""
Наследование — механизм, при котором дочерний класс получает методы и атрибуты
родительского класса. Позволяет переиспользовать код.

Синтаксис:
  class Child(Parent):
      pass

super() — способ вызвать метод родительского класса из дочернего.
Используется в __init__ для инициализации родительской части объекта.

super().__init__(args) — вызывает __init__ родителя
self передаётся автоматически, указывать не нужно

Порядок поиска методов — MRO (Method Resolution Order):
  Child → Parent1 → Parent2 → ... → object
Определяется алгоритмом C3-линеаризации.
"""

class Animal:
    def __init__(self, name):
        self.name = name
    
    def speak(self):
        return f"{self.name} издаёт звук"

class Dog(Animal):
    def __init__(self, name, breed="дворняга"):
        super().__init__(name)  # вызываем Animal.__init__
        self.breed = breed
    
    def speak(self):  # переопределяем метод родителя
        return f"{self.name} говорит: Гав!"

