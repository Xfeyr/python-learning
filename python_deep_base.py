# 6. @property — МЕТОД КАК АТРИБУТ
# =============================================================================
"""
@property — декоратор, который позволяет вызывать метод БЕЗ СКОБОК,
как будто это обычный атрибут.

Используется для:
  1. Вычисляемых свойств (площадь, диаметр — зависят от других атрибутов)
  2. Проверок состояния (is_alive — зависит от hp)
  3. Защиты от изменений (getter есть, setter запрещён)

Преимущества:
  - Читается естественно: circle.area вместо circle.area()
  - Всегда актуальное значение (вычисляется на лету)
  - Нельзя случайно изменить: circle.area = 10 → AttributeError

Под капотом @property — это дескриптор.
"""

import math

class Circle:
    def __init__(self, radius):
        self.radius = radius
    
    @property
    def diameter(self):
        """Диаметр = 2 × радиус."""
        return self.radius * 2
    
    @property
    def area(self):
        """Площадь = π × r²."""
        return math.pi * self.radius ** 2

