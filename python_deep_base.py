# 7. ГЕНЕРАТОРЫ И yield
# =============================================================================
"""
Генератор — это функция, которая может ОСТАНАВЛИВАТЬСЯ и ПРОДОЛЖАТЬ позже.
Вместо return использует yield.

Как работает:
  1. Вызов функции-генератора возвращает объект-генератор (не выполняет код!)
  2. next(генератор) выполняет код до ПЕРВОГО yield
  3. yield возвращает значение и СТАВИТ ФУНКЦИЮ НА ПАУЗУ
  4. Следующий next() продолжает с ТОГО ЖЕ МЕСТА
  5. Когда yield'ов больше нет — StopIteration

Преимущества:
  - Экономия памяти: не хранит весь список в памяти, выдаёт по одному
  - Бесконечные последовательности: yield может никогда не закончиться
  - Асинхронность: основа для Event Loop и asyncio

Генераторное выражение (аналог list comprehension):
  (x**2 for x in range(10))  — генератор
  [x**2 for x in range(10)]  — список (весь в памяти)
"""

def countdown(n):
    """Обратный отсчёт от n до 1."""
    while n > 0:
        yield n
        n -= 1

def fibonacci(n):
    """Числа Фибоначчи до n-го включительно."""
    a, b = 0, 1
    for _ in range(n + 1):
        yield a
        a, b = b, a + b

def even_numbers(limit):
    """Только чётные числа от 0 до limit."""
    for i in range(0, limit + 1, 2):
        yield i

def chunks(lst, size):
    """Разбивает список на куски заданного размера."""
    for i in range(0, len(lst), size):
        yield lst[i:i + size]

