# 1. TYPE И OBJECT — ОСНОВА ВСЕГО
# =============================================================================
"""
В Python ВСЁ — объект. Даже классы — это объекты.

type — метакласс. Это класс, который создаёт другие классы.
object — базовый класс, от которого наследуются все классы.

Ключевые отношения:
  - type создаёт все классы (включая object и самого себя)
  - object — родитель всех классов (включая type)
  - Это замкнутый круг, реализованный на уровне C в интерпретаторе

Примеры:
  type(A)       → <class 'type'>        класс A создан метаклассом type
  type(object)  → <class 'type'>        даже object создан type
  type(type)    → <class 'type'>        type создаёт сам себя
  A.__bases__       → (<class 'object'>,)   A наследуется от object
  object.__bases__  → ()                    object — вершина, нет родителей
  isinstance(A, type)      → True    A это экземпляр type
  isinstance(object, type) → True    object это экземпляр type
  issubclass(type, object) → True    type наследуется от object
  issubclass(A, object)    → True    любой класс наследуется от object

Практический смысл:
  - Понимание, как устроен Python изнутри
  - Основа для метаклассов
  - Осознание, что class — это синтаксический сахар над type()
"""

