# 8. EVENT LOOP НА ГЕНЕРАТОРАХ
# =============================================================================
"""
Event Loop (цикл событий) — бесконечный цикл, который переключается между задачами.
Основа для asyncio, JavaScript event loop, игровых движков.

Как работает:
  1. Есть список задач-генераторов, каждая хранит время пробуждения
  2. Цикл проверяет: если задача "спит" (wake_time > now) — пропускает
  3. Если задача проснулась — делает next(task)
  4. Задача через yield возвращает, на сколько секунд уснуть
  5. Цикл обновляет wake_time = now + sleep_seconds
  6. Если задача завершилась (StopIteration) — удаляем из списка

Ключевая идея: КООПЕРАТИВНАЯ многозадачность.
Задачи САМИ решают, когда уступить управление (через yield).
В отличие от потоков, нет гонок данных и блокировок.

Сравнение с asyncio:
  asyncio внутри устроен ТОЧНО ТАК ЖЕ.
  Только await = yield from, и добавлена работа с сокетами (через select/epoll).
"""

import time

class EventLoop:
    def __init__(self):
        self.tasks = []  # [генератор, wake_time]
    
    def create_task(self, gen):
        """Добавить задачу. Она начнёт выполняться сразу (wake_time = сейчас)."""
        self.tasks.append([gen, time.time()])
    
    def run(self, max_time=5.0):
        """Крутить цикл, пока есть задачи и не вышло время."""
        start = time.time()
        while self.tasks and time.time() - start < max_time:
            now = time.time()
            for task, wake_time in self.tasks[:]:  # копия списка для безопасного удаления
                if wake_time > now:
                    continue  # задача ещё спит
                try:
                    sleep_sec = next(task)  # выполняем один шаг задачи
                except StopIteration:
                    self.tasks.remove([task, wake_time])  # задача завершена
                else:
                    if sleep_sec is not None:
                        # Обновляем время пробуждения этой задачи
                        for i, (t, _) in enumerate(self.tasks):
                            if t is task:
                                self.tasks[i][1] = now + sleep_sec
                                break

